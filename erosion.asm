# R1 x
# R2 y

INIT:
# CONSTANTS
  LI R7 19
  LI R6 20

XLOOP:
  LI R2 0
YLOOP:
  MULI R3 R2 20
  ADD R3 R3 R1

  JZ SETBLACK R1
  JEQ SETBLACK R1 R7
  JZ SETBLACK R2
  JEQ SETBLACK R2 R7

  LD R4 R3

  JZ WPIXEL R4

  # x - 1
  SUBI R5 R3 1
  LD R5 R5
  JZ SETBLACK R5

  # x + 1
  ADDI R5 R3 1
  LD R5 R5
  JZ SETBLACK R5

  # y - 1
  SUBI R5 R3 20
  LD R5 R5
  JZ SETBLACK R5

  # y + 1
  ADDI R5 R3 20
  LD R5 R5
  JZ SETBLACK R5

  JR WPIXEL
SETBLACK:
  LI R4 0
WPIXEL:
  ADDI R3 R3 400
  SD R4 R3
CONTINUE:
  ADDI R2 R2 1
  JNE YLOOP R2 R6

  ADDI R1 R1 1
  JNE XLOOP R1 R6
HALT:
  END